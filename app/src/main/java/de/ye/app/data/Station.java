public class Station {

    private String id;
    private String name;
    private String type;


    public String getId() {
        return this.id;
    }


    public String getName() {
        return this.name;
    }


    public String getType() {
        return this.type;
    }


    public void setId(String id) {
        this.id = id;
    }


    public void setName(String name) {
        this.name = name;
    }


    public void setType(String type) {
        this.type = type;
    }
}
